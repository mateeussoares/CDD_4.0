#Ler 2 números, efetuar as 4 operações matemáticas e mostrar os resultados.
n1 = int(input("Informe um número:"))
n2 = int(input("Informe outro número:"))

add = n1+n2
sub = n1-n2
div = n1/n2
mul = n1*n2

print(f"Resultados:\n"
      f"Adição: {add}\n"
      f"Subtração: {sub}\n"
      f"Divisão: {div}\n"
      f"Multiplicação: {mul}")


